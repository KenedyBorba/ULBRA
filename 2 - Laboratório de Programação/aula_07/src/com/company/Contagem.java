package com.company;

public class Contagem {

    public static void main(String[] args) {
        for (int i=1; i<=10;i++){
            System.out.println(i);
        }
    }

    public static void contar(int inicio, int fim) {
        for (int i= inicio; i<=fim;i++){
            System.out.println(i);
        }
    }
}
